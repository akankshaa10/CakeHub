<h2><center>Oreo Cake Recipe</center></h2><br>

<b>Ingredients -</b><br>
<b>Cake :</b>
<p>1 cup all-purpose flour<br>
1/2 cup Oreo cookie crumbs sift first then measure to remove larger chunks<br>
1 1/2 cups granulated sugar<br>
6 Tbsp Dutch-processed cocoa powder sifted<br>
6 Tbsp black cocoa powder<br>
1 1/2 tsp baking soda<br>
1 tsp baking powder<br>
1 tsp salt<br>
1/4 cup vegetable oil<br>
3/4 cup buttermilk<br>
3/4 cup hot water or hot coffee<br>
2 large eggs<br>
2 tsp vanilla</p><br>

<b>Oreo Buttercream:</b>
<p>6 large egg whites<br>
2 cup granulated sugar<br>
2 cups unsalted butter room temperature, cubed<br>
1 tsp clear vanilla extract optional<br>
1 cup Oreo cookie crumbs sifted</p><br>

<b>Dark Chocolate Ganache:</b>
<p>3 oz good quality dark chocolate chopped<br>
3 oz heavy cream<br>
1-2 drops black color gel optional</p><br>

<b>Assembly:</b>
<p>10 Oreos chopped<br>
mini Oreos optional</p>


<b>Instructions -</b><br><br>
<b>Oreo Cake:</b>
<p>Preheat oven to 350F, grease three 6" round baking pans and dust with cocoa powder. Line bottoms with parchment.
Place all dry ingredients into the bowl of a stand mixer fitted with a paddle attachment. Stir to combine.
In a medium bowl whisk all wet ingredients (pour coffee in slowly as not to cook the eggs if very hot).
Add wet ingredients to dry and mix on medium for 2-3 mins. Batter will be very thin.<br><br>
Pour evenly into prepared pans. I used a kitchen scale to ensure the batter is evenly distributed.
Bake until a cake tester comes out mostly clean. A total of 30-35mins.
Cool 10minutes in the pans then turn out onto a wire rack to cool completely. Once cooled, cut each layer in half horizontally to get 6 layers. ·êß</p><br>

<b>Oreo Buttercream:</b>
<p>Place egg whites and sugar into the bowl of a stand mixer, whisk until combined.*
Place bowl over a hot water bath on the stove and stir with a whisk until the mixture is no longer grainy to the touch (approx. 3mins).
Place bowl on your stand mixer and whip on med-high until the meringue is stiff and cooled (the bowl is no longer warm to the touch (approx. 5-10mins)).<br><br>
Switch to paddle attachment. Slowly add cubed butter and mix until smooth.
Add vanilla and whip until smooth.** Remove about 1 cup of buttercream and set aside.
Add Oreo cookie crumbs to the remainder of the buttercream and whip until smooth.</p><br>

<b>Dark Chocolate Ganache:</b>
<p>Add cream to a medium microwave safe bowl. Add 1-2 drops of black color gel and stir to combine (optional). Add chopped chocolate and microwave in 10second intervals, stirring in between, until completely smooth and shiny. Set aside to cool completely and thicken before using as a drip.</p><br>

<b>Assembly:</b>
<p>Place one layer of cake on a cake stand or serving dish. Top with about 2/3 cup Oreo buttercream and sprinkle with chopped Oreos. Repeat with remaining layers. Do a think crumb coat all over the cake and chill for 20mins.
Frost and smooth the outside of the cake with the remaining Oreo buttercream. Chill for 30mins until firm.<br><br>
Using a small spoon, place dollops of ganache around the top edges of the cooled cake, allowing some to drip down. Fill in the top of the cake with more ganache and spread evenly with an offset spatula. Allow to set for a few minutes.
Fit a piping bag with a 1M piping tip. Fill with the white buttercream that was set aside. Pipe rosette dollops on top of the cake. Place a mini Oreo in the center of each dollop if desired.</p>

