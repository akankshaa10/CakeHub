<p>Standardized recipes are used every day in
child nutrition operations as a guide to
preparing the foods served to customers.
A standardized recipe is a recipe that has
been carefully adapted and tested to ensure
that it will produce a consistent product
every time it is used.

Why is it important to use standardized
recipes in child nutrition operations?</p>

<p><b>• Consistent Food Quality:</b> Standard-
ized recipes help ensure that the best
possible food items are produced every
time.</p>

<p><b>• Predictable Yield:</b> Knowing how
much of an item the recipe will
produce helps prevent food waste and
shortages on the serving line.</p>

<p><b>• Accurate Nutrient Content:</b>
Standardized recipes help ensure
that the best possible food items are
produced every time.</p>

<p><b>• Food Cost Control:</b> When the same
ingredients and quantities are used
each time the recipe is prepared, the
cost per serving remains the same.</p>

<p><b>• Efficient Purchasing:</b> The quantity
of ingredients needed for production
can be easily calculated based on the
information provided on the recipe.</p>

<p><b>• Inventory Control:</b> If a standardized
recipe is followed, the quantity of food
inventory used each time the recipe is
produced can be estimated.</p>

<p><b>• Labor Cost Control: Written proce-</b>
dures allow workers to make good use of
their time and move through their work
day more efficiently.</p>

<p><b>• Employee Confidence:</b> Workers can
feel more satisfied and confident because
eliminating guesswork decreases the
likelihood of mistakes and poor food
quality.</p>

<p><b>• Reduced Record Keeping:</b>
Information from standardized recipes
can be easily transferred to daily food
production records.</p>

