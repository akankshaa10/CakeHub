<h2><center>Chocolate Caramel Cake Recipe</center></h2><br>

<b>Ingredients -</b><br>
<b>Cake: </b>
<p>2 cups sugar<br>
1 3/4 cup all purpose flour<br>
2 tablespoons cornstarch<br>
3/4 cup dark cocoa powder<br>
2 teaspoons baking soda<br>
1 teaspoon salt<br>
2 eggs<br>
1 egg yolk<br>
1 cup vegetable oil<br>
2 teaspoons vanilla<br>
1/2 cup coffee<br>
1/2 cup boiling water<br>
1 cup buttermilk</p><br>

<b>Caramel Sauce :</b>
<p>1 3/4 cup sugar<br>
1/4 cup water<br>
1 stick butter room temperature<br>
1 1/2 cups whipping cream room temperature</p><br>

<b>Caramel Filling</b>
<p>3/4 cup crushed pretzels</p><br>

<b>Caramel Frosting</b>
<p>2-3 tablespoons milk<br>
1/3 cup unsalted butter<br>
1/3 cup shortening<br>
3-4 cups powdered sugar</p><br>

<b>Chocolate Ganache</b>
<p>3/4 cup dark chocolate chips<br>
1/2 tablespoon light corn syrup<br>
1/2 cup heavy whipping cream</p><br>


<b>Instructions -</b><br>

<p>To make cake, preheat over to 350 degrees. Line 3, 8" pans with parchment paper. Flour & butter sides of pans (I use baking Pam to do this).
Whisk together first six ingredients in mixing bowl. Then add following 7 ingredients and beat on medium speed until all ingredients well combined, about 2 minutes. The batter is thin.<br><br>
Divide batter evenly among the three prepared pans.
Bake until toothpick comes out of center of cake with just a few crumbs, about 25 minutes.
While cake is baking, make caramel sauce. In small pot over medium heat, add sugar and water. Continue stirring until sugar melts (it will turn grainy, then harden, and then melt), this will take about 10 minutes. Once sugar melts, stop stirring, turn heat to medium-high and boil until mixture turns an amber color and has a nutty smell. Remove from heat.<br><br>
Cut butter into cubes and gently place in sauce. Whisk until smooth. Slowly pour in whipping cream, continuously whisking. Note: It is important that the butter and cream are not cold, if they are cold it will make the caramel harden before it's well mixed.
Allow caramel to cool to room temperature. Note: This will make about 3 cups of caramel sauce.
To make filling, crush pretzels until you about about 3/4 cup of crushed pretzels.
Mix pretzels with 1 1/4 cups of caramel sauce. Set aside.<br><br>
To make frosting, beat butter and sugar together and light and fluffy. Pour in 1 cup of caramel sauce and beat until smooth. Gradually add powdered sugar, 1/2 cup at a time, until desired sweetness level is reached. Mix in milk to desired consistency.
Assemble: First level off domes of cooled cakes. Then, use caramel frosting to make a barrier around the outside edge of the top two cake layers. Use half of the pretzel/caramel filling to spread inside the barrier of each of these layers. Stack layers, ending with non-frosted layer.<br><br>
Use caramel frosting to frost outside of cake. I do a thin layer and then put it in the fridge for about 15 minutes (this is a crumb layer), and then finish. Save about 1/2-1 cup for decor.
Make ganache. Place chocolate chips and corn syrup in heat proof bowl. Heat heavy cream in microwave until boiling (about 30 seconds in my microwave), and then pour over chocolate mixture. Cover bowl with plastic wrap and let sit for 5 minutes. After this, whisk together until smooth.<br><br>
To place ganache on cake you may pour in center and spread with offset spatula, or place in bottle so that you can allow the ganache to run down the sides, and then filling top of cake with remaining ganache.
Allow ganache to cool for about 5-10 minutes. Decorate with remaining caramel frosting. Place remaining caramel sauce in drizzle bottle and decorate as desired. NOTE: Above recipe are links of things I use to make this process of cake assembly and decor easier.
</p>
