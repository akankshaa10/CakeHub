<h2><center>Chocolate Icecreame Cake Recipe</center></h2><br>

<b>Ingredients -</b><br>
<b>CHOCOLATE CAKE: </b>
<p>2 sticks (4 ounces) unsalted butter, room temperature<br>
 1 cup sugar<br>
 4 large eggs<br>
 1 1/2 cups all purpose flour<br>
 1 tablespoon baking powder<br>
 Pinch of salt<br>
 1/2 cup cocoa powder<br>
 1 cup milk<br>
 1 tablespoon vanilla extract</p><br>

<b>CHOCOLATE MOUSSE:</b>
<p>3 egg yolks<br>
 7 tablespoons sugar, divided<br>
 1/2 cup milk<br>
 8 ounces Bittersweet chocolate, roughly chopped<br>
 1 envelope (0.25 ounces) gelatin powder<br>
 1 cup heavy cream, cold<br>
 1 teaspoon vanilla extract</p><br>

<b>CHOCOLATE ICE CREAM:</b>
<p>About 3 cups chocolate ice cream (Suggested: The Curious Creamery German Chocolate)</p><br>

<b>CHOCOLATE WHIPPED CREAM:</b>
<p>2 cups heavy cream<br>
 4 ounces finely chopped semisweet chocolate, roughly chopped</p><br>

<b>CHOCOLATE GANACHE: (OPTIONAL)</b>
<p>1 cup heavy cream<br>
 1/2 cup bittersweet chocolate, chopped<br>
 Garnish:Chocolate curls/shavings</p><br>


<b>Instructions -</b><br>
<b>CHOCOLATE CAKE:</b>

<p>Preheat oven to 350F degrees and butter and flour a 9X13 cake pan.
Sift together the flour, baking powder and salt into a bowl. Using a spoon, mix well to combine. Reserve.
Using a stand mixer, beat the butter and sugar until pale and fluffy. Then, add the eggs, one at a time, beating well in between additions.<br><br>
Start adding the dry ingredients, alternating with the milk. Once everything is added and smooth, add the vanilla extract and beat just until incorporated.
Pour the batter into the prepared pan.
Bake in the preheat oven for 30 to 40 minutes or until a toothpick inserted in the center comes out clean. Cool 10 minutes; remove from pans to wire racks to cool completely. 
</p><br>

<b>CHOCOLATE MOUSSE:</b>
<p>In a medium-sized bowl, whisk the egg yolks with 5 tablespoons of sugar until it becomes a pale cream.
In a saucepan, over medium heat, heat the milk and 2 tablespoons of sugar until the sugar is dissolved and the milk starts to boil. Pour the milk very slowly over the yolk cream, whisking constantly, to temper. Then transfer the yolk/milk mixture back to the saucepan and cook over low heat until 179F degrees or when the cream coats the back of a spoon.<br><br>
While the yolk cream is cooking, place the bittersweet chocolate in a bowl and melt in the microwave (or double boiler) in 30 second increments, being careful not to burn it.
Once the cream is ready, pour over the chocolate and mix or whisk until smooth. Add the gelatin powder and whisk until incorporated. Reserve.
Using a hand (or stand mixer), beat the cold heavy cream and vanilla extract until soft peak forms.
Add 1/3 of the whipped cream to the chocolate mixture and mix vigorously. Then, delicately fold the remaining 2/3 of whipped cream, being careful not to deflate it. Reserve.</p><br>

<b>ASSEMBLING THE CAKE:</b>
<p>Cut the cake in half and using a 12-inch bread pan as a guide, cut the cake into two long rectangles, making it 4 thin layers. You are going to use only 3, so feel free to snack on this delicious cake while you work! ;-)
Line the bread pan with parchment paper, making sure it's sticking out so it's easier to pull at the end. Then, add the first layer of cake, patching any open spaces with broken pieces of cake, if necessary. Add the mousse, smoothing it with a spatula, and bring to the freezer for at least 20 minutes for it to set.<br><br>
Once the mousse layer has set, add the second layer of cake and, on top of that, the ice cream layer. (If using The Curious Creamery Ice Cream Mix, bring to the freezer until solid before adding the last layer of cake!) Finish off with the last layer of cake, wrap with plastic and freeze for at least 2 hours.</p><br>

<b>CHOCOLATE WHIPPED CREAM:</b>
<p>In a small saucepan, combine heavy cream and chocolate, and heat over medium heat until chocolate has melted. Remove from heat and refrigerate until well chilled.<br>
Place heavy cream mixture in the bowl of a stand mixer fitted with the whisk attachment; beat until medium peaks form. Pour it over the chocolate ice cream cake and spread with a spatula. Serve immediately or put it back in the freezer if adding chocolate ganache and chocolate shavings.</p><br>

<b>CHOCOLATE GANACHE: (OPTIONAL)</b>
<p>Heat the heavy cream in a saucepan until it begins to steam.
Pour over the chopped chocolate in a bowl. Let sit for 5 minutes and the mix with a spoon.<br>
Once it has cooled down slightly, pour it over the cake, spreading with a spatula so it falls along the sides of the cake.
Decorate with the chocolate shavings and serve.</p>
