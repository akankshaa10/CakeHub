<h2><center>Eggless Butterscotch Cake Recipe</center></h2><br>

<p>This Ultimate eggless butterscotch cake has a soft base with moist and creamy whipped cream filling flavoured with butterscotch essence and topped with praline to give a crunchy texture that just melts your heart with every bite.</p>

<p><b>Course :</b>Dessert<br>
 <b>Cuisine:</b> Indian<br>
 <b>Prep Time :</b> 1 hour<br>
 <b>Cook Time :</b> 30 minutes<br>
 <b>Chilling Time :</b> 30 minutes<br>
 <b>Servings :</b> 6 inch cake<br>
 </p><br>

 <b>Ingredients -</b><br><br>
 <b>Cake Base</b><br>
 <p>1.5 cups Plain Flour/ Maida (refer notes)<br>
2 tbsp Cornflour<br>
1.5 tsp Baking Powder<br>
1/2 tsp Baking Soda<br>
pinch of salt<br>
1 cup Yogurt<br>
1 cup Sugar<br>
1/2 cup Oil<br>
1/4 cup Milk<br>
1 tsp Butterscotch Essence</p><br>

<b>Sugar Syrup</b><br>
 <p>1/4 cup Boiling Hot Water<br>
1/4 cup Sugar</p>

<b>Whipped Cream Filling</b><br>
 <p>200 gms Whipping Cream<br>
30 gms Icing Sugar<br>
half of the prepared butterscotch sauce<br>
1/4 tsp Butterscotch Essence</p><br>

<b>Almond Praline</b><br>
 <p>20 gms Sugar<br>
5 gms Water<br>
7 gms Almonds chopped</p><br>

<b>Final Frosting</b><br>
 <p>75 gms Cream Cheese<br>
250 gms Whipping Cream<br>
63 gms Icing Sugar<br>
1/2 tsp Butterscotch Essence</p><br>

<b>Instructions -</b><br><br>
<b>Cake Base</b><br>
<p>1. Measure out the prepared plain flour in a bowl. Take out 2 tbsp of the flour and then add 2 tbsp cornflour. This is the step to make your own homemade cake flour. (However you can also use 1.5 cups of cake flour directly without doing the additional step if you prefer)</p>
<p>2. Add in the baking powder, baking soda and salt and sift everything together. Mix using a whisk and set aside.</p>
<p>3. In a separate bowl, combine together the yogurt and sugar until homogenous. Whisking continuously add in the oil and milk and whisk until everything comes together. Finally add in the essence and give a final mix.</p>
<p>4. Transfer the dry ingredients in 2 batches and combine until a smooth batter is formed.</p>
<p>5. Divide evenly between 3 - 6 inch cake tins that has been buttered and floured and pre lined with parchment paper.</p>
<p>6. Bake in a preheated oven of 180Â°C for 30-40 mins or until a skewer inserted in the centre comes out clean.</p>
<p>7. Allow it to rest in the tin for 3 mins and then invert it into a cooling rack to cool completely.</p>

<b>Sugar Syrup</b><br>
<p>1. Mix the sugar in the boiling water and stir to dissolve it completely. Let is cool in the refrigerator until ready to use.</p>

<b>Praline</b><br>
<p>1. Combine sugar and water in a saucepan. The sugar will start bubbling and melting away and will also start to caramalize which can be indicated by the change of colour that you can observe through.</p>
<p>2. Once the sugar syrup turns golden brown in colour take it off the flame, add in the chopped almonds give a quick stir and transfer to a cookie tray lined with aluminium foil. Spread it out immediately to form a thin layer and allow it to harden at room temperature.</p>


<b>Whipped Cream Filling</b><br>
<p>1. Take all the ingredients mentioned under cream filling tab and whip until stiff peaks form.</p>

<b>To Assemble</b><br>
<p>1. Once the cake is cooled completely trim off the top layer to level it even though you have a flat top cake as this step helps the cake to absorb all the sugar syrup you would add and keeps the cake moist for 3 days.</p>
<p>2. Next soak the cake layer with the prepared sugar syrup especially the corners of the cake</p>
<p>3. Add in one third of the prepared whipped cream filling and spread it out evenly.</p>
<p>4. Top it with a swirl of butterscotch sauce
Crush the praline to bite size pieces reserving some for final decoration and top it over the sauce layer.</p>
<p>5. Repeat the cake layers until you reach the top. With the same leftover cream crumb coat the cake and let it chill in the fridge for 30 mins.</p>

<b>Final Frosting</b><br>
<p>1. Take the cream cheese out of the fridge, weigh out the required amount, microwave for 10 sec and then proceed with the recipe.</p>
<p>2. Beat the cream cheese until smooth and creamy. Whipping continuously add the whipping cream until combined. Finally add the sugar and essence and whip until stiff peaks form</p>
<p>3. After 30 mins frost the cake completely with the stabilized whipped cream frosting till you get a smooth finish on the sides and top. Refer the video on how to frost your cake for a better understanding. Decorate the way you prefer.</p><br><br>

