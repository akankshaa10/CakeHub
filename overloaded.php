<h2><center>Chocolate Overloaded Cake Recipe</center></h2><br>

<b>Ingredients -</b><br>
<b>For the cake:</b>
<p>70 grams dark chocolate chocolate with at least 70 % cocoa, chopped<br>
110 grams all purpose flour<br>
10 grams cornflour<br>
20 grams cocoa powder<br>
1/2 teaspoon baking powder<br>
1/4 teaspoon baking soda<br>
1 teaspoon salt<br>
100 grams yoghurt<br>
15 grams water<br>
100 grams butter<br>
200 grams sugar<br>
2 eggs</p><br>

<b>For the white chocolate cheesecake:</b>
<p>80 grams white chocolate chopped<br>
300 grams cream cheese I use Philadelphia<br>
2 eggs + 1 yolk<br>
250 grams confectioner's sugar<br>
2 tablespoons all-purpose flour<br>
1 teaspoon vanilla extract<br>
1/4 cup lemon juice</p><br>

<b>For the ganache:</b>
<p>200 grams dark chocolate 70% cocoa<br>
2/3 cup whipping cream<br>
1 teaspoon butter</p><br>

<b>For the hazelnut biscuit base:</b>
<p>1/4 cup hazelnuts<br>
200 grams hazelnut cookies (or any cookie of your choice)<br>
70 grams softened butter</p><br>


<b>Instructions -</b><br>


<p><b>To make the chocolate cake:</b><br>
Preheat the oven to 150 C. Prepare a 7-inch cake pan by brushing it with butter and dusting the pan with flour. Tap off the excess flour.
Start by melting the dark chocolate Place a pan with an inch of water on the stove to heat. Once it starts to simmer, put the chopped dark chocolate in a heat-proof bowl and place this over the simmering water to melt. Alternatively, melt the dark chocolate in brief increments in a microwave.<br><br>
Sieve together the flour, cornflour, cocoa powder, baking powder, baking soda and salt and keep this aside.
Whisk together the yoghurt and water in a small bowl and keep it aside.
In a large mixing bowl, using a hand-held mixer or a stand mixer, beat the butter and sugar together until the butter has lightened in colour.
Beat in one egg at a time, mixing well after adding each egg.<br><br>
Add the melted and cooled dark chocolate and mix it in.
On low speed, alternately mix the dry ingredients and the beaten yoghurt until fully mixed into the batter. Start and end with the dry ingredients.
Bake for 45-40 minutes until a skewer inserted comes out clean. By baking at a low temperature (150 C) for a longer time, the cake rises evenly and doesn't have a mound in the centre.<br><br>

<b>To prepare the white chocolate cheesecake:</b><br>
Prepare a springform pan by lining the bottom and sides with baking parchment.
Melt the white chocolate (similar to melting the dark chocolate) and keep aside to cool.
Preheat the oven to 160 C.<br><br>
In a stand mixer or hand-held mixer, beat the cream cheese on high speed until fluffy. Add the flour slowly and mix on low speed till incorporated.
To this, add all the remaining ingredients and beat at low speed, until everything is well incorporated.
Prepare a water bath for the chesecake so that the surface does not crack. For this you need to line the springform pan with at least two layers of aluminium foil, and then place it in a baking tray filled with water (around 1 cm deep). Bake the cheesecake for 1 hour at 160 C untill set.<br><br>

<b>To prepare the ganache:</b><br>
Using a double boiler, melt the chocolate, while the chocolate is still hot, add the cream and butter and whisk it. The ganache sets as it cools, so while it is at room temperature and still fluid, pour it over the cake and let it drip over the sides.<br><br>

<b>To prepare the hazelnut biscuit base:</b><br>
Coarsely grind the hazelnuts and the cookies. Add the ground cookies and nuts to a bowl along with the softened butter and bring the mixture together like you would a dough. In a springform pan, press this mixture into the base of the pan and let it set in the fridge for at least 2 hours till set.<br><br>

<b>To Assemble:</b>
Make sure that the cake and cheesecake are cool, and the biscuit base is set in the fridge. Gently take out the biscuit base, and place it on a cake pan Place the cake over it and the cheesecake over the cake. While the ganache is still semi-fluid, spoon it over the cake and using an off-set spatula, spread it over the top, letting it drip. Top the cake with assorted chocolates (I used Lindt bars and hazelnut cookies, and handmade chocolate truffles).
</p><br>

