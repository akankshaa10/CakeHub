<h2><center>Raspberry Chocolate Cake Recipe</center></h2><br>

<b>Ingredients -</b><br>
<b>RASPBERRY FILLING: </b>
<p>1/4 cup (52g) sugar<br>
1 tbsp cornstarch<br>
2 tsp water<br>
2 cups (225g) raspberries*</p><br>

<b>CHOCOLATE CAKE:</b>
<p>1 3/4 cups (228g) all purpose flour<br>
2 cups (414g) sugar<br>
3/4 cup (85g) natural unsweetened cocoa<br>
2 1/4 tsp baking soda<br>
1/2 tsp baking powder<br>
1 tsp salt<br>
1 cup (240ml) milk<br>
1/2 cup (120ml) vegetable oil<br>
1 1/2 tsp vanilla extract<br>
2 large eggs<br>
1 cup (240ml) hot water</p><br>

<b>CHOCOLATE FROSTING:</b>
<p>1 1/2 cups (336g) unsalted butter, room temperature<br>
12 oz semi sweet chocolate chips, melted<br>
5 tbsp (35g) Hershey’s dark cocoa powder<br>
6 cups (690g) powdered sugar<br>
1/2 tsp salt<br>
10–12 tbsp (150-180ml) heavy whipping cream</p><br>

<b>CHOCOLATE WHIPPED CREAM:</b>
<p>2 cups heavy cream<br>
 4 ounces finely chopped semisweet chocolate, roughly chopped</p><br>

<b>CHOCOLATE GANACHE: </b>
<p>9 oz (1 1/2 cups) semi-sweet chocolate chips<br>
3/4 cup heavy whipping cream</p><br>


<b>Instructions -</b><br>
<b>TO MAKE THE RASPBERRY FILLING :</b>

<p>1. Combine the sugar, cornstarch and water in a medium-sized saucepan over medium heat and stir to combine. Heat until the mixture is wet and the sugar begins to melt.<br>
2. Add the raspberries and stir to coat with the sugar mixture.<br>
3. Continue to cook until the raspberries start to soften and let out juice. Use your spatula to mash the raspberries.<br>
4. Remove from heat when raspberry juice has begun to thicken, after about 5 minutes. Pour the mixture into another bowl and refrigerate until cool.<br>
</p><br>

<b>TO MAKE THE CAKE LAYERS :</b>
<p>5. Preheat oven to 350°F (176°C) and prepare three 8 inch cake pans with non-stick baking spray and parchment paper in the bottom.<br>
6. Add the flour, sugar, cocoa, baking soda, baking powder and salt to a large mixer bowl and combine. Set aside.<br>
7. Add the milk, vegetable oil, vanilla extract and eggs to a medium sized bowl and combine.<br>
8. Add the wet ingredients to the dry ingredients and beat until well combined.<br>
9. Slowly add the hot water to the batter and mix on low speed until well combined. Scrape down the sides of the bowl as needed to make sure everything is well combined.<br>
10. Divide the batter evenly between the prepared cake pans and bake for 22-25 minutes, or until a toothpick comes out with a few moist crumbs.<br>
11. Remove cakes from oven and allow to cool for 2-3 minutes, then remove to a cooling rack to finish cooling.</p><br>

<b>TO MAKE THE CHOCOLATE FROSTING :</b>
<p>12. Add the butter to a large mixer bowl and beat until smooth.<br>
13. Add the melted chocolate and mix until well combined.<br>
14. Add the cocoa powder and mix until well combined.<br>
15. Add about half of the powdered sugar, salt and half of the heavy cream and mix until well combined.<br>
16. Add the remaining powdered sugar and mix until smooth.<br>
17. Add the remaining heavy cream as needed to get the right consistency of frosting.</p><br>

<b>TO MAKE THE GANACHE AND FINISH THE CAKE :</b>
<p>18. To make the chocolate ganache, add the chocolate chips to a medium sized bowl (see my tips on making chocolate ganache).<br>
19. Heat the heavy whipping cream in the microwave until it just begins to boil. Keep an eye on it because it happens quickly.<br>
20. Pour the hot cream over the chocolate chips and let it sit for 2-3 minutes, then whisk until smooth. Set aside.<br>
21. To layer the cake, first use a large serrated knife to remove the domes from the tops of the cakes so they are flat. See my tips on how to level a cake and how to stack a cake.<br>
22. Place the first cake layer on a serving plate or on a cardboard cake circle.<br>
23. Pipe a dam around the outside of the cake layer with some of the chocolate frosting.<br>
24. Spread about 1/4 cup of chocolate ganache on top of the cake, in the center of the dam.<br>
25. Drop spoonfuls of the raspberry filling (use about half of the raspberry filling per layer) over the chocolate ganache and then spread into an even layer.<br>
26. Add the second layer of cake, then repeat steps 23 thru 25.<br>
27. Add the third and final layer of cake on top.<br>
28. Frost the top and outside of the cake with the remaining chocolate frosting.<br>
29. Use the remaining chocolate ganache to drizzle chocolate around the edges of the cake, then fill in the center. If the ganache has gotten a little firm from sitting, just reheat for a few seconds until pourable again. See my tips on making a chocolate drip cake.<br>
30. Pipe swirls with the remaining chocolate frosting around the top of the cake, then decorate with additional raspberries and chocolate sprinkles, if desired.<br>

Refrigerate the cake until ready to serve. The cake is best when served at room temperature and holds up well for 4-5 days when well sealed in an air tight container.<br>

*You’ll need about 2 pints of raspberries for the filling and decor.</p>
